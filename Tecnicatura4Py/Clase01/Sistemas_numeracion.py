#Profundizando en los sistemad e numeracion
#Sistema decimal
a = 10
print(f"a decimal: {a}")
#Sistema binario
a = 0b1010
print(f"a binario: {a}")
#Sistema octal
a = 0o12
print(f"a octal: {a}")
#Sistema hexadecimal
a = 0xA
print(f"a hexadecimal: {a}")

#Conversiones
a = int('23', 10)
print(f"a decimal: {a}")

a = int('1010', 2)
print(f"a binario: {a}")

a = int('12', 8)
print(f"a octal: {a}")

a = int('A', 16)
print(f"a hexadecimal: {a}")

# Base 5
a = int('20', 5)
print(f"a base 5: {a}")


